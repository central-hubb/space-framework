<h1>General Git Commands</h1>

<p>These are the general git commands. Below it shows how to use them on the command line</p>
<p>If you use source tree the buttons across the top menu in source tree can run these command actions for you</p>

<table class="table table-striped">
    <tr>
        <th style="width:250px;">Command</th>
        <th>Description</th>
    </tr>
    <tr>
        <td>git clone</td>
        <td>This will download a git repository to your computer</td>
    </tr>
    <tr>
        <td>git add * -A</td>
        <td>This will add files to prepare to upload to a repository</td>
    </tr>
    <tr>
        <td>git commit -m 'my message'</td>
        <td>This will add a message to write to when pushed to a repository</td>
    </tr>
    <tr>
        <td>git push</td>
        <td>This will push / upload your files to the github repository</td>
    </tr>
    <tr>
        <td>git pull</td>
        <td>This will pull all 'new changes' of code by yourself and other developers to your computer from git</td>
    </tr>
</table>
